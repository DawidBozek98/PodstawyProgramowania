# Tworzymy krotkę z wartościami
my_tuple = (50, 20, 40, 50, 30, 50)

# Określamy wartość, której liczba wystąpień ma być zliczona
value_to_count = 50

# Liczymy wystąpienia wartości w krotce
occurrences = my_tuple.count(value_to_count)


print("Tuple:", my_tuple)
print("Value:", value_to_count)
print("Number of occurrences:", occurrences)
