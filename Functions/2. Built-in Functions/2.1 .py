# Program korzystający z wbudowanych funkcji Pythona

# Znajdowanie największej liczby z podanych
max_number = max(7, 5, 6, 3, 8, 2)
print('Max number of 7,5,6,3,8,2 is', max_number)

# Znajdowanie najmniejszej liczby z podanych
min_number = min(4, 7, 2, 3, 9, 8)
print('Min number of 4,7,2,3,9,8 is', min_number)

# Obliczanie długości ciągu znaków
str_length = len("computer science")
print('The number of characters in "computer science" is', str_length)

# Wczytanie litery z klawiatury
letter = input('Enter a letter: ')  
print('You entered:', letter)

# Liczba reprezentująca ciąg znaków "20303"
number_from_string = int("20303")  
print('The number from the string "20303" is', number_from_string)

# Reprezentacja binarna liczby dziesiętnej 304
binary_representation = bin(304)  
print('Binary representation of 304 is', binary_representation)

# Reprezentacja szesnastkowa liczby dziesiętnej 304
hexadecimal_representation = hex(304)  
print('Hexadecimal representation of 304 is', hexadecimal_representation)

# Kod Unicode dla znaku €
unicode_code = ord('€')  
print('The Unicode code for the € sign is', unicode_code)

# Wartość bezwzględna liczby 
absolute_value = abs(-17)  
print('The absolute value of -17 is', absolute_value)
